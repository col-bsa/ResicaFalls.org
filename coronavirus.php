                <p>
                    Dear Friends of Resica Falls,
                    </p>

                    <p>
                    For most Scouts, summer camp is the highlight of the Scouting year. With that in mind, key volunteers across several council committees, along with the summer camp and council leadership teams, have spent a tremendous amount of time and effort monitoring updates and projections by several government, Scouting, and camping organizations from the local to national levels. The goal was to seek a path forward to be able to conduct our summer programs in a manner that will keep our Scouts and leaders safe, maintaining our council’s unwavering commitment to the well-being of our members. 
                    </p>

                    <p>
                    Regrettably, we have come to the difficult decision to cancel all in-person summer camp operations for 2020. This decision was not arrived at lightly, but it is aligned with and supports the council’s ongoing commitment to the safety of our Scouts and leaders above all else. Knowing how important an outdoor experience is to our youth, we will continue to monitor state and local guidance with the hope of being able to offer small-group gatherings later in the summer.
                    </p>

                    <p>
                    Knowing how important a summer program is for your Scouts, we will be offering a new type of camp program this summer to continue to provide the same Scout-oriented experience that is synonymous with Resica Falls. <strong>2020 Virtual Camp @ Resica Falls</strong> will be a whole new level of adventure that we are excited to be able to offer to our Scouts and units this summer. We are here for you. While many finer details are still being developed, there are some things we can tell you right now:
                    </p>
                    <ul>
                        <li>We will still be following our theme of “Under the Big Top,” and many of our camp-wide activities will focus on this theme.</li>
                        <li>Scouts will have the opportunity to take up to 6 merit badges throughout the week and receive instruction from our specially-trained counselors.</li>
                        <li>Extensive camp-wide programs will be offered, from lunch clubs, to camp-wide games, along with a Scout’s Own Service, an OA Ice Cream Social, and of course – opening & closing campfires.</li>
                        <li>Every Scout will receive a welcome packet in the mail with all the necessary program supplies to complete the badges they choose and to be a part of our camp experience.</li>
                        <li>We will be offering extensive programs and training for adult leaders as well!</li>
                    </ul>

                    <p>
                    The staff is working hard to lock in details and we will email you updates as they become available. Unit leaders can learn more about Virtual Camp by attending the Pre-Camp Leaders Meeting on Monday, May 4th at 7pm. <a href="https://us02web.zoom.us/webinar/register/WN_krch0GiMQYGhaTsbc1q7HQ" target="_blank">Click this link to register to join us.</a>
                    </p>

                    <p>
                    We will be answering many questions during the Pre-Camp Leader’s Meeting on Monday, and a full FAQ will be posted to www.resicafalls.org/coronavirus over the weekend. For additional questions, program questions, please reach-out to <a href="mailto:campdirector@resicafalls.org">Chris Brenner at campdirector@resicafalls.org</a>, and if you have refund questions, please reach-out to <a href="mailto:tracy.launders@scouting.org">Tracy Launders at tracy.launders@scouting.org</a>. For general questions about Cradle of Liberty Camping operations, feel free to reach-out to <a href="mailto:john.bickel@scouting.org">John Bickel at john.bickel@scouting.org</a> or (484) 614-2370.
                    </p>

                    <p>
                    While this is not how any of us expected the summer of 2020 to go, the volunteers and staff of the Cradle of Liberty Council remain committed to providing programs to help you and your Scouts make the most of these unprecedented times. We remain hopeful that we will be able to provide small-group programs later in the summer as government guidelines allows, and we eagerly look ahead to the incredible line up of fall programs that are planned! Until then, please stay healthy and safe and we hope to see you soon along the virtual trail.  
                    </p>

